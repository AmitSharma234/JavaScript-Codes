//comparison operators
let a=5;
let b=2;
console.log("a==b",a==b);  //5==2//false

console.log("a!=b",a!=b);  //5!=2//true

let c=6;
let d="6";
console.log("c===d",c===d);   //6==="6"//false


console.log("c!==d",c!==d);   //6==="6"//false

console.log("a>b",a>b);    //5>3//true

console.log("a<b",a<b);    //5<3//false